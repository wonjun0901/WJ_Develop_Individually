# 관계연산자
# - 크기의 대소를 비교하여 진리형(참/거짓) 값으로
#   반환하는 연산자
# - >, <, >=, <=, ==(같음), !=(같지않음)

# - 파이썬의 진리형 값은 True, False로 지정

n1 = 10
n2 = 5

r = n1 > n2
print(f"{n1} > {n2} = {r}")

r = n1 < n2
print(f"{n1} < {n2} = {r}")

r = n1 >= n2
print(f"{n1} >= {n2} = {r}")

r = n1 <= n2
print(f"{n1} <= {n2} = {r}")

r = n1 == n2
print(f"{n1} == {n2} = {r}")

r = n1 != n2
print(f"{n1} != {n2} = {r}")









