# -*- coding: utf-8 -*-

# 파이썬에서 기본 출력을 위해서 사용되는 함수
# - 기본출력 : 모니터(콘솔)
# - print(출력값)

# 정수형 값을 출력
# 정수형 : 숫자로만 구성된 값
print(10)

# 실수형 값을 출력
# 실수형 : 소수점 이하의 값을 가지는 수치 값
print(51.7)

# 진리형 값을 출력
# 진리형 : True(참), False(거짓)의 값을 의미
print(True)
print(False)

# 문자열 타입의 값을 출력
# 문자열 : ", ', ''', """ 로 정의된 값을 의미
print("문자열1")
print('문자열2')
print('''문자열3''')
print("""문자열4""")














